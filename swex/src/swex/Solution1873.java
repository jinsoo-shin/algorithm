	static boolean cango(int[] current) {// 갈수있는가
		int x = current[0];
		int y = current[1];
		String tmp = map[x][y];
		if (tmp.equals("^")) {
			if (x - 1 >= 0 && map[x - 1][y].equals(".")) {// 평지
				return true;
			}
		} else if (tmp.equals("<")) {
			if (y - 1 >= 0 && map[x][y - 1].equals(".")) {// 평지
				return true;
			}
		} else if (tmp.equals(">")) {
			if (y + 1 < map[0].length && map[x][y + 1].equals(".")) {// 평지
				return true;
			}
		} else if (tmp.equals("v")) {
			if (x + 1 < map.length && map[x + 1][y].equals(".")) {// 평지
				return true;
			}
		}
		return false;
	}

